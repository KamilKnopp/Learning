package Kamil.learning;

public class Priklad2 {
    public static void main(String args[])
    {
        int var1;
        double var2;

        var1 = 10;
        var2 = 10.0;

        System.out.println("Hodnota var1 je: " + var1);
        System.out.println("Hodnota var2 je: " + var2);
        System.out.println();

        var1 = var1/4;
        var2 = var2/4;

        System.out.println("Hodnota var1 po deleni: " + var1);
        System.out.println("Hodnota var2 po deleni: " + var2);
    }
}
